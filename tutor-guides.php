          <h2>Concerns from Students</h2>
        </div>
        <p>
          When asking students to conduct self and peer assessment, most of them resist. Such resistance is due to the following major reasons:
          <ul>
            <li>Most students think that their peers do not do the peer assessment fairly and responsibly.</li>
            <li>Students feel that do not have sufficient knowledge and skill to assess their peers' work.</li>
            <li>Students worry that if they gave low marks or negative comments to their classmates, their friendship with peers would be adversely affected.</li>
            <li>Students worry that if they gave low marks or negative comments to their classmates, their classmates might give low marks or negative comments to them as well.</li>
            <li>Students think that it is embarrassed to tell the others their mistakes or weaknesses.</li>
          </ul>
          Regarding to student presentation, many students do not pay attention to others’ presentation. Some of the reasons included:
          <ul>
            <li>The presentations of the other groups are not interesting.</li>
            <li>Students think that they cannot learn technical knowledge from the other groups' presentations.</li>
            <li>Students think that they cannot learn presentation and communication skills from the other groups' presentations.</li>
            <li>The other groups’ presentations are not important to study.</li>
          </ul>
        </p>
