        <div class="page-header">
          <h2>Scoring Method of Intra-group Self and Peer Assessment</h2>
        </div>
        <p>
          Many scoring methods have been used by teachers when conducting peer and self-assessment. Most of them use likert scale from 5 (excellent) to 0 (unacceptable) or 3 (good) to 0 (not helpful). To enable students to rate their peers’ performance in a simple but precise manner, a 5-point scale ranging from 3 to -1 is implemented in the CAPP2 system. In the intra-group assessment, each student has to give a score to each member of his/her group, including the student himself/herself, for the contribution to each criterion. The highest score to be given is 3 and the lowest is -1, as below:
          <ul class="list-unstyled">
            <li>3 = better than most of the group in this respect</li>
            <li>2 = about average in this respect</li>
            <li>1 = not as good as most of the group in this respect</li>
            <li>0 = no help at all in this aspect</li>
            <li>-1 = a hindrance to the group in this respect</li>
          </ul>
          The marks awarded for each member will be used to calculate the individual mark for the project.
        </p>
        <p>Following the tutor’s defined parameters, including smoothening condition and penalty-setting to those students to do not perform self and/or peer assessments, a PA score (to individual student) will be calculated by the CAPP2 system automatically.</p>
