          <h2>Scoring Method of the Inter-group Peer Assessment</h2>
        </div>
        <p>
          To avoid bias when students rating other groups’ performance, relative ranking will be used as the scoring method instead of absolute grading (or assigning mark). Each student has to give a rank to other groups in accordance to their relative performance. For example, assuming there are 5 groups of presentations (A, B, C, D and E), each group consists of 4 students. All students of group A has to give a rank to group B, C, D and E. The better the group performance, the higher would be the mark. As a result, student 1 of group A may give the following assessment results:
        </p>
        <ul>
            <li>Group B – Rank 1: the best (4 marks will be assigned to Group B by the system);</li>
            <li>Group C – Rank 3 (2 marks will be assigned);</li>
            <li>Group D – Rank 2 (3 marks will be assigned) and</li>
            <li>Group E – Rank 4: the worst (1 mark will be assigned)</li>
        </ul>
        <p>
            Following the tutor’s defined parameters, such as setting the upper and lower score limits and the penalty to students who do not performed peer assessment, a peer group mark will be calculated by the CAPP2 system automatically.
        </p>
